using System;
using System.Collections.Generic;
using System.Text;

namespace Classes_Exercise
{
    class Vector2
    {
        public float xPosition;
        public float yPosition;

        public Vector2()
        {
            xPosition = 0;
            yPosition = 0;
        }

        public Vector2(float xPos, float yPos)
        {
            xPosition = xPos;
            yPosition = yPos;
        }
    }
}
